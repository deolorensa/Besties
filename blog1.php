 <h1>Anjing menjadi akrab dengan majikan</h1>
 <p>Jika anjing peliharaan tampak ketakutan atau enggan berada di dekat Anda,
 fokuskan diri untuk memperbaiki hubungan Anda dengannya. Pastikan ia berada di lingkungan 
 yang aman dengan membangun ruang yang aman dan nyaman untuknya di rumah. Arahkan ia dengan 
 cara melatihnya dan memberikan hadiah positif. Setelah ia merasa terawat dan disayangi, akan 
 lebih mudah baginya untuk mengakrabkan dirinya dengan Anda. Ajak ia berjalan-jalan, bermainlah bersamanya, 
 dan biarkan ia menemani Anda. Seiring berjalannya waktu, anjing Anda akan menyukai interaksinya dengan Anda.</p>
 <p>Anda mungkin bisa mengetahui saat ia menyukai interaksi kecil seperti ini. Sebagai contoh, anjing Anda mungkin menjulurkan lidahnya atau melihat Anda dengan tatapan penuh sayang saat Anda mengusap telinganya.
    Catatan: Anjing tidak senang saat wajahnya diusap atau kepalanya ditepuk-tepuk. Anda juga tidak boleh memberinya pelukan yang terlalu erat karena ia akan merasa tak nyaman.</p>
